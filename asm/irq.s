    pop r10
    pop r9
    pop r8
    pop rdi
    pop rsi
    pop rbp
    pop rdx
    pop rcx
    pop rbx
    pop rax

    add rsp, 0x10

    iretq

;; macro for interrupt requests
%macro IRQ 2
[GLOBAL isr%2]
isr%2:
    push qword 0
    push qword %1
    jmp irq_common_stub
%endmacro

